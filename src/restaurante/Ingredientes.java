    public void set (Plato p) {
        String a = "ingredientes: \n";
        LinkedList c = p.getIngredientes();
        Ingrediente n;
        for (int i = 0; i < c.size(); i++) {
            n = (Ingrediente) c.get(i);
            a += " \n " + n.getNombre();
        }
        label.setText(a);
    }

